#  STEP 2: Revenue Analysis Function
def analyze_sales(log):
    daily_revenue = {}
    item_frequency = {}

    for date, orders in log.items():
        day_total = 0
        for order in orders:
            day_total += order["total"]
            # Count item occurrences across all orders
            for item in order["items"]:
                item_frequency[item] = item_frequency.get(item, 0) + 1

        daily_revenue[date] = day_total

    return daily_revenue, item_frequency

