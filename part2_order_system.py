cart = []

def add_to_cart(item_name, quantity):
    # 1. Check if item exists in menu
    if item_name not in menu:
        print(f"✗ Error: '{item_name}' is not on the menu.")
        return

    # 2. Check if item is available
    if not menu[item_name]["available"]:
        print(f"✗ Error: '{item_name}' is currently unavailable.")
        return

    # 3. Check if already in cart to avoid duplicates
    for entry in cart:
        if entry["item"] == item_name:
            entry["quantity"] += quantity
            print(f"✓ Updated {item_name} quantity to {entry['quantity']}.")
            return

    # 4. Add new entry if not a duplicate
    cart.append({
        "item": item_name,
        "quantity": quantity,
        "price": menu[item_name]["price"]
    })
    print(f"✓ Added {quantity}x {item_name} to cart.")

def remove_from_cart(item_name):
    for i, entry in enumerate(cart):
        if entry["item"] == item_name:
            cart.pop(i)
            print(f"✓ Removed {item_name} from cart.")
            return
    print(f"✗ Error: {item_name} not found in cart.")

